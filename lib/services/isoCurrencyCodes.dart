String convertToString(IsoCurrencyCodes currency) {
  return currency.toString().split(".").last;
}

IsoCurrencyCodes convertToEnum(String currency) {
  IsoCurrencyCodes f = IsoCurrencyCodes.values
      .firstWhere((e) => e.toString() == 'IsoCurrencyCodes.$currency');

  return f;
}
